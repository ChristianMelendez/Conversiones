    private void BtnigualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnigualActionPerformed
        // TODO add your handling code here:
        switch(opcion){
            case 1 :
                numero1=Integer.parseInt(this.Txtnumero1.getText());
                 numero2=Integer.parseInt(this.Txtnumero2.getText());
                suma=numero1+numero2;
                Labelresultado.setText(String.valueOf(suma));
                break;
            case 2 :  
                numero3=Integer.parseInt(this.Txtnumero1.getText());
                 numero4=Integer.parseInt(this.Txtnumero2.getText());
                resta=numero3-numero4;
                Labelresultado.setText(String.valueOf(resta));
                break;
            case 3 :
                numero5=Integer.parseInt(this.Txtnumero1.getText());
                numero6=Integer.parseInt(this.Txtnumero2.getText());
                multiplicacion=numero5*numero6;
                Labelresultado.setText(String.valueOf(multiplicacion));
                break;
                
                
        }
    }//GEN-LAST:event_BtnigualActionPerformed

